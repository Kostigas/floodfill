let bitmap = [  "................**********........................",
                "...............*..........*.......................",
                "..........*****............*........*.............",
                ".........*.................*.......*.*....*****...",
                "........*................***......*...*.**.....**.",
                "....****.................*.......*.....*.........*",
                "..**......................*******................*",
                ".*...............................................*",
                ".*...............................................*",
                "*...........****.............................****.",
                "*..........*....*.........................***.....",
                ".*.........*....*.......................**........",
                "..***.......****.......................*..........",
                ".....****......................******..*..........",
                ".........**********************.....****.........." ]; 


bitmap = bitmap.map(row => [...row]);
xsize = bitmap[0].length;
ysize = bitmap.length;


check = (x, y) =>{
    if (y < 0 || y >= ysize) return;
    if (x < 0 || x >= xsize) return;

    
    change(x, y);
}


change = (x, y) =>{
    if (bitmap[y][x] === '*') return;

    floodFillassign(x,y);
}


floodFillassign = (x, y) => {

   
    bitmap[y][x] = '*';

    
    check(x - 1, y);
    check(x + 1, y);
    check(x, y - 1);
    check(x, y + 1);
};


check(8, 7);

bitmap = bitmap.map(row => row.join('')).join('\n');

